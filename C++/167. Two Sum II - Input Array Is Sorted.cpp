class Solution {
public:
    vector<int> twoSum(vector<int>& numbers, int target) {
        int left{};
        int right{static_cast<int>(numbers.size()-1)};
        
        while (left < right)
        {
            int sum = numbers[left] + numbers[right];
            if (sum == target)
            {
                return {left+1, right+1};
            }
            if (sum > target) 
                --right;
            if (sum < target)
                ++left;
        }
        return {};
    }
};
